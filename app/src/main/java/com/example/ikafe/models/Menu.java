package com.example.ikafe.models;

public class Menu {
    private String kode, jenis, nama, keterangan, harga;

    public Menu(String kode, String jenis, String nama, String keterangan, String harga) {
        this.kode = kode;
        this.jenis = jenis;
        this.nama = nama;
        this.keterangan = keterangan;
        this.harga = harga;
    }

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }
}
